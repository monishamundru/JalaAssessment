# Assignment-2 : Operators

# 1. Arithmetic Operators
a = 20
b = 10

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)


# 2. Increment and Decrement
a += 1
print("Increment:", a)

a -= 1
print("Decrement:", a)


# 3. Equal or Not Equal
if a == b:
    print("Both are Equal")

else:
    print("Both are Not Equal")


# 4. Relational Operators
print("a < b :", a < b)
print("a <= b :", a <= b)
print("a > b :", a > b)
print("a >= b :", a >= b)


# 5. Smaller and Larger Number
if a > b:
    print("Largest:", a)
    print("Smallest:", b)

else:
    print("Largest:", b)
    print("Smallest:", a)